import UIKit

extension UIColor {
    
    static func snapchatYellow() -> UIColor {
        return UIColor(red: 255/255.0, green: 252/255.0, blue: 0.0, alpha: 1.0)
    }
    
    static func appPurple() -> UIColor {
        return UIColor(red: 109/255.0, green: 123/255.0, blue: 245/255.0, alpha: 1.0)
    }
    
    static func appGray() -> UIColor {
        return UIColor(red: 118/255.0, green: 118/255.0, blue: 118/255.0, alpha: 1.0)
    }
    
    static func appLightGray() -> UIColor {
        return UIColor(red: 204/255.0, green: 204/255.0, blue: 204/255.0, alpha: 1.0)
    }
    
    static func appYellow() -> UIColor {
        return UIColor(red: 255/255.0, green: 202/255.0, blue: 67/255.0, alpha: 1.0)
    }
    
}
